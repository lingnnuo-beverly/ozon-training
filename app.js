function getAiBaseUrl() {
  if (typeof window === "undefined") return "";
  const override = window.AI_API_BASE;
  if (override && typeof override === "string") return override.replace(/\/+$/, "");
  if (location.protocol === "file:") return "http://localhost:3001";
  if (location.port && location.port !== "3001") return "http://127.0.0.1:3001";
  return "";
}

function aiApi(path) {
  const base = getAiBaseUrl();
  return `${base}${path}`;
}

function escapeHtml(value) {
  return String(value == null ? "" : value)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}

function formatAiAnswer(answer) {
  return escapeHtml(answer).replace(/\n/g, "<br />");
}

