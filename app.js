async function checkAiHealth() {
  const badge = $("#aiServiceStatus");
  if (!badge) return;
  badge.textContent = "检测后端中…";
  try {
    const response = await fetch(aiApi("/api/ai/health"));
    if (!response.ok) throw new Error(`HTTP ${response.status}`);
    const data = await response.json();
    const parts = [];
    parts.push(data.openaiConfigured ? "OpenAI已配置" : "OpenAI未配置");
    parts.push(data.supabaseConfigured ? "Supabase已配置" : "Supabase未配置");
    badge.textContent = parts.join(" · ");
    badge.style.background = data.openaiConfigured ? "#e2f3ea" : "#fff1c9";
    badge.style.color = data.openaiConfigured ? "#0c5d37" : "#7a5200";
  } catch (error) {
    badge.textContent = "后端未启动，请先运行 server";
    badge.style.background = "#f3e5e5";
    badge.style.color = "#b53b3b";
  }
}

